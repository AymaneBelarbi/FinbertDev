def preprocess_text(text):
    """Clean and preprocess text data for sentiment analysis."""
    if not isinstance(text, str):
        return ""
    text = text.lower()
    text = re.sub(r'http\S+', '', text)
    text = re.sub(r'@\w+', '', text)
    text = re.sub(r'[^\w\s\.\!\?\,\$\%]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text

